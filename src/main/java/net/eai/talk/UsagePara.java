package net.eai.talk;

public class UsagePara  extends Word {
	private Usage usage;
	private Word value;
	
	
	
	public Usage getUsage() {
		return usage;
	}
	public void setUsage(Usage usage) {
		this.usage = usage;
	}
	public Word getValue() {
		return value;
	}
	public void setValue(Word value) {
		this.value = value;
	}
}
